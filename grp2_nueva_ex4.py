# Task #1: Arithmetic Operations for int, float, and complex
# Integer Operations (Addition, Subtraction, Multiplication, Division, Modulus, Exponent)
num1_int = 10
num2_int = 3
print(num1_int + num2_int)
print(num1_int - num2_int) 
print(num1_int * num2_int)
print(num1_int / num2_int)
print(num1_int % num2_int)
print(num1_int ** num2_int)

# Float Operations (Addition, Subtraction, Multiplication, Division, Modulus, Exponent)
num1_float = 10.5
num2_float = 2.5
print(num1_float + num2_float)
print(num1_float - num2_float) 
print(num1_float * num2_float)
print(num1_float / num2_float)
print(num1_float % num2_float)
print(num1_float ** num2_float)

# Complex Operations (Addition, Subtraction, Multiplication, Division, Exponent)
# Note: Modulus is not supported for complex numbers in Python.
num1_complex = 4 + 2j
num2_complex = 1 + 1j
print(num1_complex + num2_complex)
print(num1_complex - num2_complex)
print(num1_complex * num2_complex)
print(num1_complex / num2_complex)
print(num1_complex ** num2_complex)

# Task #2: Integer literals with and without underscores
num1 = 25000000
num2 = 25_000_000
print(num1)
print(num2)

# Task #3: Checking int, float, and complex variable types
val_int = 100
val_float = 10.5
val_complex = 5j
print(type(val_int))
print(type(val_float))
print(type(val_complex))
